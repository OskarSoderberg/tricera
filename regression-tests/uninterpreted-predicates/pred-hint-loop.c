extern int nondet();

/*$
  p(int, int)
  $*/
void main1 () {
  int i = _;
  int n = _;
  assume(i < n);
  assert(p(i, n)); // loop init & consec.

  i = nondet(); 
  assume(i >= n);
  assume(p(i, n)); // loop exit

  assert(i == n);
}


void loop(int i, int n) {
  assume(p(i, n));
  assume(i < n);
  i += 1;
  assert(p(i, n));
}

void main() {
  if(nondet())
    main1();
  else if (nondet())
    loop(nondet(), nondet());
}
